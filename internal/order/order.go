package order

import (
	"context"
	"time"
)

// Attr is a single selected attribute on an order, ready to be persisted as a
// row in the order_attrs table.
type Attr struct {
	Name  string // rendered attribute title, e.g. "Display color"
	Value string // rendered value title, e.g. "Red"
	Price int    // per-attribute add-on price in minor units (e.g. cents)
}

// Invoice is the result of issuing a payment invoice with a provider, ready
// to be persisted into order_invoices.
type Invoice struct {
	Provider  string // e.g. "monobank"
	InvoiceID string // provider's invoice id
	PageURL   string // hosted payment page URL
	Amount    int    // minor units, mirrors the order total at issue time
	Currency  string // alpha-3 (e.g. "UAH")
}

// Order is a customer order ready to be persisted.
//
// Fields generated by the database (id, status, timestamps) are intentionally
// absent: the writer relies on column defaults.
type Order struct {
	ProductID    string
	Email        string
	Price        int // total in minor units: base price + sum of Attr.Price
	Currency     string
	FirstName    string
	MiddleName   string
	LastName     string
	Country      string
	City         string
	Phone        string
	Address      string
	Attrs        []Attr // sorted by attribute key for deterministic ordering
	CustomerNote string
}

// HistoryEntry is one row of an order's status timeline.
type HistoryEntry struct {
	ID        string
	Status    string
	Note      *string // nil when SQL NULL
	CreatedAt time.Time
}

// Record is a fully-populated order read from the store, including DB-generated
// fields and all related attrs and history entries.
type Record struct {
	ID           string
	ProductID    string
	Status       string // order_status enum, as text
	Email        string
	Price        int
	Currency     string
	FirstName    string
	MiddleName   *string // nil when SQL NULL
	LastName     string
	Country      string
	City         string
	Phone        string
	Address      string
	AdminNote    *string // nil when SQL NULL
	CustomerNote *string // nil when SQL NULL
	CreatedAt    time.Time
	UpdatedAt    time.Time
	Attrs        []Attr         // never nil; possibly empty
	History      []HistoryEntry // chronological, oldest first; never nil
	Invoices     []Invoice      // never nil; possibly empty
}

// Writer persists an order to a backing store and returns the assigned id.
type Writer interface {
	Write(ctx context.Context, o Order) (string, error)
}

// Reader reads orders from a backing store.
type Reader interface {
	List(ctx context.Context) ([]Record, error)
}

// InvoiceWriter records an issued payment invoice for an order. Implementations
// run the inserts that transition the order to status='awaiting_payment' in a
// single transaction so the schema invariant "order has invoice ↔ status is
// awaiting_payment" cannot be violated by a partial write.
type InvoiceWriter interface {
	AttachInvoice(ctx context.Context, orderID string, inv Invoice) error
}

// Service submits orders via a Writer, attaches invoices via an InvoiceWriter,
// and reads them via a Reader.
type Service struct {
	w  Writer
	r  Reader
	iw InvoiceWriter
}

// NewService returns a Service backed by w, r, and iw.
func NewService(w Writer, r Reader, iw InvoiceWriter) *Service {
	return &Service{w: w, r: r, iw: iw}
}

// Submit writes the order to the backing store and returns the assigned id.
func (s *Service) Submit(ctx context.Context, o Order) (string, error) {
	return s.w.Write(ctx, o)
}

// AttachInvoice records an issued payment invoice for the given order.
func (s *Service) AttachInvoice(ctx context.Context, orderID string, inv Invoice) error {
	return s.iw.AttachInvoice(ctx, orderID, inv)
}

// List returns all orders, newest first. Always returns a non-nil slice on
// success (possibly empty).
func (s *Service) List(ctx context.Context) ([]Record, error) {
	return s.r.List(ctx)
}
