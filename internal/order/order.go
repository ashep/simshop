package order

import (
	"context"
	"encoding/json"
	"errors"
	"time"
)

// Invoice status enum values, kept in sync with the invoice_status PostgreSQL
// enum. The webhook handler maps provider-specific status strings (e.g.
// Monobank's "success"/"failure") onto these values.
const (
	InvoiceStatusProcessing = "processing"
	InvoiceStatusHold       = "hold"
	InvoiceStatusPaid       = "paid"
	InvoiceStatusFailed     = "failed"
	InvoiceStatusReversed   = "reversed"
)

// ErrNotFound is returned by Reader implementations when the requested
// order does not exist.
var ErrNotFound = errors.New("order not found")

// Attr is a single selected attribute on an order, ready to be persisted as a
// row in the order_attrs table.
type Attr struct {
	Name  string // rendered attribute title, e.g. "Display color"
	Value string // rendered value title, e.g. "Red"
	Price int    // per-attribute add-on price in minor units (e.g. cents)
}

// Invoice is the result of issuing a payment invoice with a provider, ready
// to be persisted into order_invoices.
type Invoice struct {
	Provider string // e.g. "monobank"
	ID       string // provider's invoice id; stored in the row's primary key
	PageURL  string // hosted payment page URL
	Amount   int    // minor units, mirrors the order total at issue time
	Currency string // alpha-3 (e.g. "UAH")
}

// Order is a customer order ready to be persisted.
//
// Fields generated by the database (id, status, timestamps) are intentionally
// absent: the writer relies on column defaults.
type Order struct {
	ProductID    string
	Email        string
	Price        int // total in minor units: base price + sum of Attr.Price
	Currency     string
	FirstName    string
	MiddleName   string
	LastName     string
	Country      string
	City         string
	Phone        string
	Address      string
	Attrs        []Attr // sorted by attribute key for deterministic ordering
	CustomerNote string
}

// HistoryEntry is one row of an order's status timeline.
type HistoryEntry struct {
	ID        string
	Status    string
	Note      *string // nil when SQL NULL
	CreatedAt time.Time
}

// InvoiceEvent is one provider-side event for an invoice (typically a
// webhook delivery). It is the input to RecordInvoiceEvent. The persisted
// timeline is later used to derive the order's payment status.
type InvoiceEvent struct {
	OrderID   string
	InvoiceID string
	Provider  string          // e.g. "monobank"
	Status    string          // one of the InvoiceStatus* constants
	Note      string          // human-readable summary (rendered by the caller)
	Payload   json.RawMessage // verbatim provider payload, persisted as JSONB
	EventAt   time.Time       // provider's modifiedDate / event timestamp
}

// Record is a fully-populated order read from the store, including DB-generated
// fields and all related attrs and history entries.
type Record struct {
	ID           string
	ProductID    string
	Status       string // order_status enum, as text
	Email        string
	Price        int
	Currency     string
	FirstName    string
	MiddleName   *string // nil when SQL NULL
	LastName     string
	Country      string
	City         string
	Phone        string
	Address      string
	AdminNote    *string // nil when SQL NULL
	CustomerNote *string // nil when SQL NULL
	CreatedAt    time.Time
	UpdatedAt    time.Time
	Attrs        []Attr         // never nil; possibly empty
	History      []HistoryEntry // chronological, oldest first; never nil
	Invoices     []Invoice      // never nil; possibly empty
}

// Writer persists an order to a backing store and returns the assigned id.
type Writer interface {
	Write(ctx context.Context, o Order) (string, error)
}

// Reader reads orders from a backing store.
type Reader interface {
	List(ctx context.Context) ([]Record, error)
	// GetStatus returns the current order_status (as text) for the given
	// order id. Returns ErrNotFound when no row matches.
	GetStatus(ctx context.Context, orderID string) (string, error)
}

// InvoiceWriter records an issued payment invoice for an order. Implementations
// run the inserts that transition the order to status='awaiting_payment' in a
// single transaction so the schema invariant "order has invoice ↔ status is
// awaiting_payment" cannot be violated by a partial write.
type InvoiceWriter interface {
	AttachInvoice(ctx context.Context, orderID string, inv Invoice) error
}

// InvoiceEventWriter persists a provider-side invoice event and, in the same
// transaction, recomputes the order's payment status from the latest event in
// the invoice timeline (ordered by EventAt). When the latest event implies a
// forward order_status transition, the order row and order_history are
// updated; otherwise the row is recorded but the order is left untouched.
//
// Inserting an event with the same (InvoiceID, Provider, Status, EventAt) as
// an existing row is a silent no-op (idempotent webhook replay).
type InvoiceEventWriter interface {
	RecordInvoiceEvent(ctx context.Context, evt InvoiceEvent) error
}

// Service submits orders via a Writer, attaches invoices via an InvoiceWriter,
// and reads them via a Reader.
type Service struct {
	w   Writer
	r   Reader
	iw  InvoiceWriter
	iew InvoiceEventWriter
}

// NewService returns a Service backed by w, r, iw, and iew.
func NewService(w Writer, r Reader, iw InvoiceWriter, iew InvoiceEventWriter) *Service {
	return &Service{w: w, r: r, iw: iw, iew: iew}
}

// Submit writes the order to the backing store and returns the assigned id.
func (s *Service) Submit(ctx context.Context, o Order) (string, error) {
	return s.w.Write(ctx, o)
}

// AttachInvoice records an issued payment invoice for the given order.
func (s *Service) AttachInvoice(ctx context.Context, orderID string, inv Invoice) error {
	return s.iw.AttachInvoice(ctx, orderID, inv)
}

// List returns all orders, newest first. Always returns a non-nil slice on
// success (possibly empty).
func (s *Service) List(ctx context.Context) ([]Record, error) {
	return s.r.List(ctx)
}

// GetStatus returns the current order_status for orderID. Returns ErrNotFound
// when no row matches.
func (s *Service) GetStatus(ctx context.Context, orderID string) (string, error) {
	return s.r.GetStatus(ctx, orderID)
}

// RecordInvoiceEvent persists a provider-side invoice event and recomputes
// the order's payment status. See InvoiceEventWriter for the full contract.
func (s *Service) RecordInvoiceEvent(ctx context.Context, evt InvoiceEvent) error {
	return s.iew.RecordInvoiceEvent(ctx, evt)
}
